//IIC
sbit lcd_sclk =P3^0;  //SCLK //SCLK 接到“D0”脚
sbit lcd_sda =P3^1;   //SDA //SDIN 接到“D1  D2”脚
sbit RST=P3^2;        // Reset

//延时
void delay(int i)
{
	int j,k;
	for(j=0;j<i;j++)
	for(k=0;k<110;k++);
}
 
void start_flag()
{
	lcd_sclk=1;
	lcd_sda=1;
	lcd_sda=0;
	lcd_sclk=0;
}
void stop_flag()
{
	lcd_sclk=0;
	lcd_sda=0;
	lcd_sda=1;
	lcd_sclk=1;
}

//传8位指令或数据到OLED显示模块
void transfer(uchar data1)
{
	unsigned char j;
	for(j=0;j<8;j++)
	{
		lcd_sclk=0;
		if(data1&0x80) lcd_sda=1;
		else lcd_sda=0;
		data1<<=1;
		lcd_sclk=1;
	}
	lcd_sclk=0;
	lcd_sclk=1;
}
 

//写指令到OLED显示模块
void transfer_command(uchar com)
{
	start_flag();
	transfer(0x78);	 //DC=0; RW=0  选择SLAVE ADDRESS
	transfer(0x00);
	transfer(com);
	stop_flag();
}
//写数据到OLED显示模块
void transfer_data(uchar dat)
{
	start_flag();
	transfer(0x78);	//DC=0; RW=0   选择SLAVE ADDRESS
	transfer(0x40);
	transfer(dat);
	stop_flag();
}

