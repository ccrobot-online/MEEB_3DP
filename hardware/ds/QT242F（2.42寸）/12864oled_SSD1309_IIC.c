//显示8x16 的点阵的字符串，括号里的参数分别为（页,列，字符串指针）
void display_string_8x16(uint page,uint column,uchar *text)
{
	uint i=0,j,k,n;
	if(column>123)
		{
		column=1;
		page+=2;
		}
	while(text[i]>0x00)
			{
			if((text[i]>=0x20)&&(text[i]<=0x7e))
				{
				j=text[i]-0x20;
				for(n=0;n<2;n++)
					{
					lcd_address(page+n,column);
					for(k=0;k<8;k++)
						{
						transfer_data(ascii_table_8x16[j][k+8*n]); //写数据到LCD,每写完1 字节的数据后列地址自动加1
						}
					}
				i++;
				column+=8;
				}
			else
			i++;
			}
}
//显示5x8 的点阵的字符串，括号里的参数分别为（页,列，字符串指针）
void display_string_5x8(uint page,uint column,uchar *text)
{
	uint i=0,j,k;
	while(text[i]>0x00)
	{	
		if((text[i]>=0x20)&&(text[i]<0x7e))
		{
			j=text[i]-0x20;
			lcd_address(page,column);
			for(k=0;k<5;k++)
			{
				transfer_data(ascii_table_5x8[j][k]);/*显示5x7的ASCII字到LCD上，y为页地址，x为列地址，最后为数据*/
			}
			i++;
			column+=6;
		}
		else
		i++;
	}
}


